class Datatypes {
	public static void main(String[] args) {
		String name = "Goutham";
		System.out.println("Student Name : " + name);

		byte age = 20;
		System.out.println("Student Age: " + age);

		short marks = 492;
		System.out.println("Student Mark : " + marks);

		int salary = 690000;
		System.out.println("Student Salary : " + salary);

		long ph_no = 919952087822L;
		System.out.println("Student Phone Number : " + ph_no);

		float height = 5.7f;
		System.out.println("Student Height : " + height);

		double cgpa = 8.2315;
		System.out.println("Student CGPA : " + cgpa);

		char gender = 'M';
		System.out.println("Student Gender: " + gender);

		boolean marriage = false;
		System.out.println("Marital status : " + marriage);
	}
}